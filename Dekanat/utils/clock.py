"""Єдине джерело «поточного часу» для застосунку (DK-50).

Проблема: `datetime.now()` віддає час у таймзоні процесу. На сервері застосунок
працює у контейнері, чия таймзона за замовчуванням UTC, тому дати створення/
редагування карток абітурієнтів і формування звітів зберігались та показувались у
UTC, а не в локальному київському часі (EET/EEST).

Рішення: скрізь, де потрібен «зараз» для збереження/порівняння/відображення,
використовуємо `now_local()` замість `datetime.now()`. Він прив'язаний до явної
зони `Europe/Kyiv` (з автоматичним переходом на літній/зимовий час) і не залежить
від таймзони ОС/контейнера. Пакет `tzdata` у залежностях гарантує наявність зони
навіть у мінімальних контейнерах.
"""

from datetime import datetime
from zoneinfo import ZoneInfo

try:
    LOCAL_TZ = ZoneInfo("Europe/Kyiv")
except Exception:  # застаріла база tzdata ще може мати лише стару назву
    LOCAL_TZ = ZoneInfo("Europe/Kiev")


def now_local() -> datetime:
    """Поточний момент у київському часі (EET/EEST) як naive datetime (wall-clock).

    Повертаємо naive навмисно: усі колонки БД — timezone-naive `DateTime`, і решта
    коду (порівняння токенів сесії, `strftime` при відображенні) розрахована саме
    на naive-значення. Заміняє `datetime.now()` по всьому застосунку."""
    return datetime.now(LOCAL_TZ).replace(tzinfo=None)
